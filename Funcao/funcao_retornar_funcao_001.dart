// Criado um função inteira que aguardar um retorno de inteiro
// é possível usar tudo que estiver do lado mais externo como a variável  c = 0
int Function(int) somaParcial(int a) {
  int c = 0;

// aqui é informado que o retorno da função soma parcial é uma função anonima int(b)
  return (int b) {
    return a + b + c;
  };
}

main() {
  print(somaParcial(2)(10));

  var somaCom10 = somaParcial(10);

  print(somaCom10(3));
  print(somaCom10(7));
  print(somaCom10(19));
}
